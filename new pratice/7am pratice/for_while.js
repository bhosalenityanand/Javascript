// ---------------- Difference between For & while Loop --------------------

for(let i=0; i<5; i++){
    console.log(i)
}

// for reverese
console.log('---------------------------------')
for(let i=5; i>=0; i--){
    console.log(i)
}

// ----------- Break
console.log('-------------------------------------------')

for(let i=0; i<10; i++){
    if(i==5){
        break
    }
    console.log(i)
}

//------------- Continue
console.log('------------------------------------------------')

for(let i=0; i<10; i++){
    if(i==5){
        continue
    }
    console.log(i)
}

